#include<bits/stdc++.h>
using namespace std;
int main() {
    // loops are used to execute a block of code for multiple times
    for(int index = 0; index <=5; index++)
    {
        //start with 0 and untill index is greater than 5 keep executing this block
        // index is called as loop counter
        cout << "Hello World" << endl;
    }
    return 0;
}