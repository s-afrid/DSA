#include<bits/stdc++.h>
using namespace std;

int main() {
    // int x; A variable is container to store different values
    //int - for numbers
    int x = 15;
    cout << x << endl;
    //long - for large numbers
    long y = 10000000000;
    cout << y << endl;
    //long long - for very large numbers
    long long h = 10000000000000;
    cout << h << endl;
    //float - for decimal numbers
    float g = 1.234;
    cout << g << endl;
    //double - for large decimal numbers
    double k = 12314293.1213243;
    cout << k << endl;
    //char - for characters in single quotes
    char a = 'k';
    cout << a << endl;
    //string -  for a group of characters in double quotes
    string b = "Programmer";
    cout << b << endl;
    //getline - for a group of strings
    string s;
    getline(cin, s);
    cout << s << endl;
    //bool - for logic true and false
    bool isMale = true;
    cout << isMale << endl;
    return 0;
}